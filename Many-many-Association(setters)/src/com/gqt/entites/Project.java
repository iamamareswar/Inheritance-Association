package com.gqt.entites;

public class Project 
{
	private int pId;
	private String pName;
	private String pMgr;

	public void setpId(int pId) {
		this.pId = pId;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public void setpMgr(String pMgr) {
		this.pMgr = pMgr;
	}
	
	public int getpId() {
		return pId;
	}
	public String getpName() {
		return pName;
	}
	public String getpMgr() {
		return pMgr;
	}
}
