package com.gqt.entities;

public class Brain
{
	private String colour;
	private int weight;
	
	public Brain(String colour, int weight) {
		super();
		this.colour = colour;
		this.weight = weight;
	}

	public String getColour() {
		return colour;
	}

	public int getWeight() {
		return weight;
	}
	
}
